from crewai import Agent, Crew, Process, Task

from dotenv import load_dotenv

from llm_config import get_llm
from tools.telegram_reader import read_telegram_channel

load_dotenv()

TASK_DESCRIPTION = (
    "Подготовь максимально полный аналитический отчёт по публичным Telegram-каналам.\n\n"
    "Ниша / тема проекта: {niche}\n"
    "Гипотеза о целевой аудитории: {target_audience}\n"
    "Каналы для анализа (через запятую или с новой строки): {competitor_channels}\n"
    "Сколько последних постов читать с каждого канала: {posts_per_channel}\n\n"
    "ОБЯЗАТЕЛЬНО: для каждого канала из списка вызови read_telegram_channel "
    "с post_limit={posts_per_channel}. Не пропускай каналы.\n\n"
    "Структура отчёта (все разделы обязательны):\n\n"
    "## 1. Резюме\n"
    "Краткие выводы на 5–7 пунктов: что происходит в нише, главные инсайты.\n\n"
    "## 2. Анализ по каждому каналу\n"
    "Для КАЖДОГО канала отдельный подраздел:\n"
    "- Название, @username, описание, подписчики (из tool)\n"
    "- Позиционирование: о чём канал, для кого\n"
    "- Темы постов: топ-5 recurring topics с примерами\n"
    "- Стиль и тон: формальный/разговорный, юмор, экспертность, эмоции\n"
    "- Форматы: короткие заметки, лонгриды, списки, новости, кейсы, опросы, медиа\n"
    "- Фишки и приёмы: заголовки, хуки, CTA, эмодзи, сторителлинг, серии постов\n"
    "- Активность: частота публикаций (по датам постов), есть ли провалы\n"
    "- Вовлечённость: просмотры постов (если есть в данных), какие посты сильнее\n"
    "- Сигналы аудитории: кого адресуют, какие боли/желания затрагивают\n\n"
    "## 3. Сравнительная таблица каналов\n"
    "Сравни каналы по: темам, тону, частоте, форматам, сильным сторонам.\n\n"
    "## 4. Портрет целевой аудитории ниши\n"
    "Обобщённый портрет ЦА на основе ВСЕХ каналов: демография (если видно), "
    "интересы, боли, мотивация, уровень экспертизы, язык общения.\n\n"
    "## 5. Активность и паттерны контента\n"
    "Когда и как часто публикуют, какие форматы доминируют, сезонные/трендовые темы.\n\n"
    "## 6. Лучшие практики и фишки ниши\n"
    "Что работает у конкурентов: 10+ конкретных приёмов с примерами из постов.\n\n"
    "## 7. Пробелы и возможности\n"
    "Чего не хватает в нише, чем можно выделиться новой группе.\n\n"
    "## 8. Рекомендации для запуска вашей группы\n"
    "5–10 практических рекомендаций по контенту, тону, рубрикам и первым постам.\n\n"
    "Пиши на русском. Будь конкретным: цитируй формулировки из постов, "
    "указывай @канал при примерах."
)

TASK_EXPECTED_OUTPUT = (
    "Полный структурированный markdown-отчёт на русском языке со всеми 8 разделами, "
    "отдельным блоком по каждому каналу и опорой на реальные данные из read_telegram_channel"
)


def build_crew(*, verbose: bool = True) -> Crew:
    llm = get_llm()

    analyst = Agent(
        role="Аналитик Telegram-ниши",
        goal=(
            "Прочитать все указанные публичные каналы и подготовить максимально полный "
            "аналитический отчёт: контент, стиль, фишки, аудитория, активность"
        ),
        backstory=(
            "Ты senior-аналитик контента в Telegram с опытом конкурентного анализа. "
            "Ты ВСЕГДА вызываешь read_telegram_channel для КАЖДОГО канала из списка "
            "до написания отчёта. Никогда не придумываешь посты, цифры и цитаты — "
            "только факты из tool. Если канал не прочитался, явно укажи это в отчёте. "
            "Сравниваешь каналы между собой и находишь закономерности."
        ),
        tools=[read_telegram_channel],
        llm=llm,
        verbose=verbose,
    )

    task = Task(
        description=TASK_DESCRIPTION,
        expected_output=TASK_EXPECTED_OUTPUT,
        agent=analyst,
    )

    return Crew(
        agents=[analyst],
        tasks=[task],
        process=Process.sequential,
        llm=llm,
        verbose=verbose,
    )


def run_analysis(
    *,
    niche: str,
    target_audience: str,
    competitor_channels: str,
    posts_per_channel: int = 20,
    verbose: bool = True,
) -> str:
    crew = build_crew(verbose=verbose)
    result = crew.kickoff(
        inputs={
            "niche": niche,
            "target_audience": target_audience,
            "competitor_channels": competitor_channels,
            "posts_per_channel": str(posts_per_channel),
        }
    )
    return str(result)
