def build_crew(*, verbose: bool = True) -> Crew:
    llm = get_llm()

    analyst = Agent(
        role="Аналитик Telegram-ниши",
        goal=(
            "Прочитать все указанные публичные каналы и подготовить максимально полный "
            "аналитический отчёт: контент, стиль, фишки, аудитория, активность"
        ),
        backstory=(
            "Ты senior-аналитик контента в Telegram с опытом конкурентного анализа. "
            "Ты ВСЕГДА вызываешь read_telegram_channel для КАЖДОГО канала из списка "
            "до написания отчёта. Никогда не придумываешь посты, цифры и цитаты — "
            "только факты из tool. Если канал не прочитался, явно укажи это в отчёте. "
            "Сравниваешь каналы между собой и находишь закономерности."
        ),
        tools=[read_telegram_channel],
        llm=llm,
        verbose=verbose,
        max_execution_time=600,
        max_iter=25,
    )

    writer = Agent(
        role="Копирайтер Telegram-постов",
        goal=(
            "Писать посты для Telegram на основе РЕАЛЬНЫХ данных из постов конкурентов — "
            "улучшая подачу, но не выдумывая факты"
        ),
        backstory=(
            "Ты копирайтер, который работает только с проверенными данными из отчёта "
            "аналитика. Ты можешь перефразировать и улучшать структуру, но запрещено "
            "придумывать цифры, кейсы, цитаты и события. Каждый пост сопровождаешь "
            "ссылкой на источник (@канал, дата, факт из отчёта)."
        ),
        llm=llm,
        verbose=verbose,
        max_execution_time=600,
        max_iter=20,
    )

    analysis_task = Task(
        description=TASK_DESCRIPTION,
        expected_output=TASK_EXPECTED_OUTPUT,
        agent=analyst,
    )

    writer_task = Task(
        description=WRITER_TASK_DESCRIPTION,
        expected_output=WRITER_TASK_EXPECTED_OUTPUT,
        agent=writer,
        context=[analysis_task],
    )

    return Crew(
        agents=[analyst, writer],
        tasks=[analysis_task, writer_task],
        process=Process.sequential,
        llm=llm,
        verbose=verbose,
    )


def _extract_crew_result(result) -> str:
    parts: list[str] = []
    tasks_output = getattr(result, "tasks_output", None) or []
    for task_output in tasks_output:
        text = (getattr(task_output, "raw", None) or str(task_output)).strip()
        if text:
            parts.append(text)
    if parts:
        return "\n\n---\n\n".join(parts)

    raw = getattr(result, "raw", None)
    if raw and str(raw).strip():
        return str(raw).strip()
    return str(result).strip()


def run_analysis(
    *,
    niche: str,
    target_audience: str,
    competitor_channels: str,
    posts_per_channel: int = 20,
    posts_to_generate: int = 10,
    verbose: bool = True,
) -> str:
    import logging

    logging.basicConfig(level=logging.INFO)
    log = logging.getLogger("analysis_crew")
    log.info(
        "Старт анализа: niche=%s channels=%s posts=%s generate=%s",
        niche,
        competitor_channels,
        posts_per_channel,
        posts_to_generate,
    )

    crew = build_crew(verbose=verbose)
    result = crew.kickoff(
        inputs={
            "niche": niche,
            "target_audience": target_audience,
            "competitor_channels": competitor_channels,
            "posts_per_channel": str(posts_per_channel),
            "posts_to_generate": str(posts_to_generate),
        }
    )

    report = _extract_crew_result(result)
    if not report:
        raise RuntimeError(
            "CrewAI вернул пустой отчёт. Проверьте OPENAI_API_KEY на Render и логи сервиса."
        )
    log.info("Анализ завершён, длина отчёта: %s символов", len(report))
    return report
